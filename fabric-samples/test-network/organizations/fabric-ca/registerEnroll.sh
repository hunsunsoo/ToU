#!/bin/bash

function createOrgProduct() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/orgProduct.tou.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/orgProduct.tou.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-orgProduct --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-orgProduct.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-orgProduct.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-orgProduct.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-orgProduct.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy org1's CA cert to org1's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem" "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/msp/tlscacerts/ca.crt"

  # Copy org1's CA cert to org1's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem" "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/tlsca/tlsca.orgProduct.tou.com-cert.pem"

  # Copy org1's CA cert to org1's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/ca"
  cp "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem" "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/ca/ca.orgProduct.tou.com-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-orgProduct --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-orgProduct --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-orgProduct --id.name orgProductadmin --id.secret orgProductadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-orgProduct -M "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/msp" --csr.hosts peer0.orgProduct.tou.com --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/msp/config.yaml"

  infoln "Generating the peer0-tls certificates"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-orgProduct -M "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls" --enrollment.profile tls --csr.hosts peer0.orgProduct.tou.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/peers/peer0.orgProduct.tou.com/tls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-orgProduct -M "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/users/User1@orgProduct.tou.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/users/User1@orgProduct.tou.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://orgProductadmin:orgProductadminpw@localhost:7054 --caname ca-orgProduct -M "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/users/Admin@orgProduct.tou.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/orgProduct/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/orgProduct.tou.com/users/Admin@orgProduct.tou.com/msp/config.yaml"
}

